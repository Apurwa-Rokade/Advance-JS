//Arithmetic Operators: These operators perform mathematical calculations.

let a = 5;
let b = 3;
let addition = a + b; // Addition (+)
let subtraction = a - b; // Subtraction (-)
let multiplication = a * b; // Multiplication (*)
let division = a / b; // Division (/)
let modulus = a % b; // Modulus (%)
let increment = a++; // Increment (++)
let decrement = b--; // Decrement (--)

//Comparison Operators: These operators compare two values and return a Boolean value.

let x = 10;
let y = 5;
console.log(x > y); // Greater than (>)
console.log(x < y); // Less than (<)
console.log(x === y); // Equal to (strict equality) (===)
console.log(x !== y); // Not equal to (!==)


//Logical Operators: These operators perform logical operations on Boolean values.

let p = true;
let q = false;
console.log(p && q); // Logical AND (&&)
console.log(p || q); // Logical OR (||)
console.log(!p); // Logical NOT (!)

//Assignment Operators: These operators assign values to variables.

let num = 10;
num += 5; // Addition assignment (+=)
num -= 3; // Subtraction assignment (-=)
num *= 2; // Multiplication assignment (*=)
num /= 4; // Division assignment (/=)

//Unary Operators: These operators work on a single operand.

let value = 5;
value = -value; // Unary negation (-)
value = +value; // Unary plus (+)
let isTrue = true;
isTrue = !isTrue; // Logical NOT (!)

//Bitwise Operators: These operators perform operations on binary representations of numbers.

let aa = 5; // Binary representation: 101
let bb = 3; // Binary representation: 011
console.log(aa & bb); // Bitwise AND (&)
console.log(aa | bb); // Bitwise OR (|)
console.log(aa ^ bb); // Bitwise XOR (^)
