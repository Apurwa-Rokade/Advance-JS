// Conditional statements are programming structures that allow you to execute certain blocks of code based
// on whether a specified condition evaluates to true or false. They help control the flow of a program by
// making decisions.

// There are mainly three types of conditional statements:

// 1. If Statement:
// The if statement executes a block of code if a specified condition is true.

// Syntax:

if (condition) {
    // Code to be executed if the condition is true
  }

//   Example:
let num = 10;

if (num > 0) {
  console.log("Number is positive");
}


// 2. If-else Statement:
// The if-else statement executes one block of code if the condition is true and another block if the 
// condition is false.

// Syntax:

if (condition) {
    // Code to be executed if the condition is true
  } else {
    // Code to be executed if the condition is false
  }

//   Example :

  let n = -5;

if (n > 0) {
  console.log("Number is positive");
} else {
  console.log("Number is not positive");
}

// 3. Else-if ladder:
// The else-if ladder allows you to check multiple conditions and execute a block of code corresponding 
// to the first true condition.

// Syntax:

if (condition1) {
    // Code to be executed if condition1 is true
  } else if (condition2) {
    // Code to be executed if condition2 is true
  } else {
    // Code to be executed if all conditions are false
  }

